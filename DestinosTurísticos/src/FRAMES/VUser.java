    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String provincia = jComboBox1.getSelectedItem().toString();
        String precio = jComboBox2.getSelectedItem().toString();
        ArrayList<ArrayList<Object>> Destinos = new ArrayList<>();
        if(provincia.equals("Cualquiera") && precio.equals("Cualquiera")){
            //Se muestran todos los destinos
            Destinos = a.obtenerTodos(); 
            String[][]dests = obtenerMatriz(Destinos);
                    
            String[] columnas = {"Nombre", "Provincia", "Precio", "Actividades", "Descripción", "Horario", "Calificación","Promoción", "Contacto","Foto"};
            DefaultTableModel model = new DefaultTableModel(dests, columnas);
            jTable2.setModel(model);    
        }
        else if (!provincia.equals("Cualquiera") && precio.equals("Cualquiera")){
            //Se busca solo por provincia
            Destinos = a.ObtenerPorProvincia(provincia);            
            String[][]dests = obtenerMatriz(Destinos);          
            
            String[] columnas = {"Nombre", "Provincia", "Precio", "Actividades", "Descripción", "Horario", "Calificación","Promoción", "Contacto","Foto"};
            DefaultTableModel model = new DefaultTableModel(dests, columnas);
            jTable2.setModel(model);             
                              
        }
        else if(provincia.equals("Cualquiera") && !precio.equals("Cualquiera")){
            //Se busca solo por precio
            Destinos = a.ObtenerPorPrecio(Double.valueOf(precio));
            String[][]dests = obtenerMatriz(Destinos);         
            
            String[] columnas = {"Nombre", "Provincia", "Precio", "Actividades", "Descripción", "Horario", "Calificación","Promoción", "Contacto","Foto"};
            DefaultTableModel model = new DefaultTableModel(dests, columnas);
            jTable2.setModel(model); 
        }
        else{
            //Se busca por ambos
            Destinos = a.ObtenerPorProvinciaYPrecio(provincia, Double.valueOf(precio));
            String[][]dests = obtenerMatriz(Destinos);          
            
            String[] columnas = {"Nombre", "Provincia", "Precio", "Actividades", "Descripción", "Horario", "Calificación","Promoción", "Contacto","Foto"};
            DefaultTableModel model = new DefaultTableModel(dests, columnas);
            jTable2.setModel(model); 
        } 
    }//GEN-LAST:event_jButton1ActionPerformed

