    public String[][] obtenerMatriz(ArrayList<ArrayList<Object>> Destinos){
        
        String matriz[][] = new String[Destinos.size()][10];        
                
        for (int i = 0; i < Destinos.size()-1; i++) {            
            matriz[i][0] = Destinos.get(i).get(0).toString();    
            matriz[i][0] = Destinos.get(i).get(0).toString();             
            matriz[i][1] = Destinos.get(i).get(1).toString();                      
            matriz[i][2] = Destinos.get(i).get(2).toString();          
            matriz[i][3] = Destinos.get(i).get(3).toString();            
            matriz[i][4] = Destinos.get(i).get(4).toString();            
            matriz[i][5] = Destinos.get(i).get(5).toString();            
            matriz[i][6] = Destinos.get(i).get(6).toString();            
            matriz[i][7] = Destinos.get(i).get(7).toString();           
            matriz[i][8] = Destinos.get(i).get(8).toString();            
            matriz[i][9] = Destinos.get(i).get(9).toString();          
        }        
        return matriz;
    }

