    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // TODO add your handling code here:
        //SE DEBE VERIFICAR QUE EXISTA LA CANTIDAD DEL PRODUCTO INDICA EN ESA SUCURSAL. SI NO, NO PERMITE AÑADIR
        int cantidadSolicitada;
        cantidadSolicitada = (int) cantidad_txt.getValue();
        String codigop = prd_codigo_txt.getText();
        String sucCodigo = sucursal_factura_txt.getText();
        
        if (!codigop.isEmpty()) {
            if (cantidadSolicitada > 0) {
                int stockproducto;
                try {
                    stockproducto = pmanager.obtenerCantidadEnStock(codigop, sucCodigo);
                    if (stockproducto >= cantidadSolicitada) {
                        ResultSet rs;
                        try {
                            rs = pmanager.obtenerProductoPorCodigo(codigop);
                            model.setColumnIdentifiers(columnNames);
                            while (rs.next()) {
                                String codigo = rs.getString("PRD_CODIGO");
                                String descripcion = rs.getString("PRD_DESCRIPCION");
                                // Para este ejemplo, se usarán valores fijos o calculados para las columnas no disponibles en la base de datos
                                String unidadMedida = "Unidad"; // Este valor puede ser calculado o derivado
                                int cantidad = 1; // Por defecto 1, esto podría ser un parámetro o venir de otro input
                                double valorUnitario = rs.getDouble("PRD_PRECIO");
                                double subtotal = cantidadSolicitada * valorUnitario;
                                subtotalf += subtotal;
                                String pagaiva = rs.getString(7);
                                if (pagaiva.equals("S")) {
                                    total += (subtotal + (subtotal * (0.15)));
                                } else {
                                    total += subtotal;
                                }
                                model.addRow(new Object[]{codigo, descripcion, unidadMedida, cantidadSolicitada, valorUnitario, subtotal, pagaiva});
                            }

                            tabla.setModel(model);
                            tabla.setRowHeight(50);

                        } catch (SQLException ex) {
                            Logger.getLogger(VentanaAcciones.class.getName()).log(Level.SEVERE, null, ex);
                        }
                    } else {
                        JOptionPane.showMessageDialog(null, "La cantidad solicitada supera al numéro en stock");
                    }
                } catch (SQLException ex) {
                    Logger.getLogger(VentanaFacturacion.class.getName()).log(Level.SEVERE, null, ex);
                }
                subtotal_txt.setText(String.valueOf(subtotalf));
                total_txt.setText(String.valueOf(total));
            }
            else
                JOptionPane.showMessageDialog(null, "La cantidad solicitada debe ser mayor a 0");

        }

    }//GEN-LAST:event_jButton4ActionPerformed

