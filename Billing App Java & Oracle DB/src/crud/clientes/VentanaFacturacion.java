    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        // TODO add your handling code here:
        String codigo = prd_codigo_txt.getText().toUpperCase();
        String descripcion = descripcion_txt.getText().toUpperCase();
        String cbarras = barras_txt.getText();
        String sucursal = sucursal_factura_txt.getText();
        String query = "SELECT "
                + "p.PRD_CODIGO, "
                + "p.PRD_DESCRIPCION, "
                + "pb.PRB_EXISTENCIA "
                + "FROM sucursal s "
                + "JOIN bodega b ON s.SUC_CODIGO = b.SUC_CODIGO "
                + "JOIN producto_bodega pb ON b.BOD_CODIGO = pb.BOD_CODIGO "
                + "JOIN producto p ON pb.PRD_CODIGO = p.PRD_CODIGO "
                + "WHERE s.SUC_CODIGO = '" + sucursal + "'";
        if (!codigo.isEmpty()) {
            query += " AND p.PRD_CODIGO LIKE '%" + codigo + "%'";
        }

        if (!descripcion.isEmpty()) {
            query += " AND p.PRD_DESCRIPCION LIKE '%" + descripcion + "%'";
        }

        if (!cbarras.isEmpty()) {
            query += " AND p.PRD_CODIGO_BARRA LIKE '%" + cbarras + "%'";
        }

        ResultSet rs;
        try {
            rs = fmanager.buscarConQuery(query);
            String[] columnNames = {"Codigo", "Descripcion", "Stock"};
            DefaultTableModel model = new DefaultTableModel();
            model.setColumnIdentifiers(columnNames);

            try {
                while (rs.next()) {
                    String codigoProducto = rs.getString("PRD_CODIGO");
                    String descripcionProducto = rs.getString("PRD_DESCRIPCION");
                    String existencia = rs.getString("PRB_EXISTENCIA");  // Ajusta según tus columnas reales
                    model.addRow(new Object[]{codigoProducto, descripcionProducto, existencia});
                }
                tabla_producto.setModel(model);
                tabla_producto.setRowHeight(50);
            } catch (SQLException ex) {
                Logger.getLogger(VentanaFacturacion.class.getName()).log(Level.SEVERE, null, ex);
            }
        } catch (SQLException ex) {
            Logger.getLogger(VentanaFacturacion.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton5ActionPerformed

