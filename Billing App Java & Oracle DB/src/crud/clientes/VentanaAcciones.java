    private void jButton7ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton7ActionPerformed
        // TODO add your handling code here:
        String cedulaRuc = cedula_txt.getText();
        String ciudad = ciudad_txt.getSelectedItem().toString();
        String nombre = nombre_txt.getText().toUpperCase(); // Convertir a mayúsculas
        String apellido = apellido_txt.getText().toUpperCase(); // Convertir a mayúsculas
        String correo = correo_txt.getText();
        String telefono = telefono_txt.getText();
        String direccion = direccion_txt.getText().toUpperCase(); // Convertir a mayúsculas
        String sector = sector_txt.getText().toUpperCase(); // Convertir a mayúsculas
        boolean verificador = true;
        if(cedulaRuc.isEmpty())
        {
            verificador = false;
            JOptionPane.showMessageDialog(null,"Debe ingresar una cédula");
        }
        if(ciudad.isEmpty())
        {
            verificador = false;
            JOptionPane.showMessageDialog(null,"Debe ingresar una ciudad");
        }
        if(nombre.isEmpty())
        {
            verificador = false;
            JOptionPane.showMessageDialog(null,"Debe ingresar un nombre");
        }
        if(apellido.isEmpty())
        {
            verificador = false;
            JOptionPane.showMessageDialog(null,"Debe ingresar un apellido");
        }
        if(correo.isEmpty())
        {
            verificador = false;
            JOptionPane.showMessageDialog(null,"Debe ingresar un correo");
        }
        if(telefono.isEmpty())
        {
            verificador = false;
            JOptionPane.showMessageDialog(null,"Debe ingresar un teléfono");
        }
        if(direccion.isEmpty())
        {
            verificador = false;
            JOptionPane.showMessageDialog(null,"Debe ingresar una dirección");
        }
        if(sector.isEmpty())
        {
            verificador = false;
            JOptionPane.showMessageDialog(null,"Debe ingresar un sector");
        }
        try {
            if(verificador)
            {
                if(telefono.length()<9)
                {
                    verificador = false;
                    JOptionPane.showMessageDialog(null,"Debe ingresar un teléfono válido");
                }
                if (!(correo.contains("@") && correo.contains(".")))
                {
                    verificador = false;
                    JOptionPane.showMessageDialog(null,"Correo inválido");
                }
                if(verificador)
                {
                    manager.insertarCliente(cedulaRuc, ciudad, nombre, apellido, correo, telefono, direccion, sector);
                }
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, e.toString());
        }
    }//GEN-LAST:event_jButton7ActionPerformed

