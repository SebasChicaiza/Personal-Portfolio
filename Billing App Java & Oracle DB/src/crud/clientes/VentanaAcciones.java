    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        ResultSet rs;
        try {
            rs = manager.verTodosLosClientes();
            String[] columnNames = {"Cédula/Ruc", "Código Ciudad", "Nombre", "Apellido", "Correo", "Teléfono", "Dirección", "Sector"};
            DefaultTableModel model = new DefaultTableModel();
            model.setColumnIdentifiers(columnNames);
            while (rs.next()) {
                String cedulaRuc = rs.getString("cli_cedula_ruc");
                String ciudad = rs.getString("ciu_codigo");
                String nombre = rs.getString("cli_nombre");
                String apellido = rs.getString("cli_apellido");
                String correo = rs.getString("cli_correo");
                String telefono = rs.getString("cli_telefono");
                String direccion = rs.getString("cli_direccion");
                String sector = rs.getString("cli_sector");

                model.addRow(new Object[]{cedulaRuc, ciudad, nombre, apellido,correo,telefono,direccion,sector});
            }
            tabla.setRowHeight(50);
            tabla.setModel(model);
        } catch (SQLException ex) {
            Logger.getLogger(VentanaAcciones.class.getName()).log(Level.SEVERE, null, ex);
        }

    }//GEN-LAST:event_jButton1ActionPerformed

